const q2 = (calculate) => {
  const answer = [];
  const array = calculate.split('').map((a) => Number(a) || a);

  const calculator = {
    '+': (a, b) => { return a + b },
    '-': (a, b) => { return a - b },
    '*': (a, b) => { return a * b },
    '/': (a, b) => { return a / b },
  }

  array.forEach((item) => {
    if (Number(item)) {
      answer.push(item);
    } else {
      const second = answer.pop();
      const first = answer.pop();
      const result = calculator[item](first, second);
      answer.push(result);
    }
  });

  return answer[0];
};

